var regex = {
    password: /^(((?=.*[a-z])(?=.*[A-Z]))|((?=.*[a-z])(?=.*[0-9]))|((?=.*[A-Z])(?=.*[0-9])))(?=.{8,})/,
    email: /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
    alfabeto: /^[A-Za-z\s']{3,}$/,
    matricola: /^[A-Za-z0-9]{4,}$/,
    conSpazio: /^[A-Za-z0-9\s,.]{3,}$/,
    codiceErasmus: /^[0-9]{3,}/,
    codiceMateria: /^[0-9]{2,}/,
    data: /^([0-2][0-9]|(3)[0-1])(\/)(((0)[0-9])|((1)[0-2]))(\/)\d{4}$/,
    telefono: /^[0-9]{8,}/,
}